package utils;

public class MyRectangle {
    private int l, r ,u, d;

    public MyRectangle(int l, int r, int u, int d) {
        this.l = l;
        this.r = r;
        this.u = u;
        this.d = d;
    }

    public int getL() {
        return l;
    }

    public void setL(int l) {
        this.l = l;
    }

    public int getR() {
        return r;
    }

    public void setR(int r) {
        this.r = r;
    }

    public int getU() {
        return u;
    }

    public void setU(int u) {
        this.u = u;
    }

    public int getD() {
        return d;
    }

    public void setD(int d) {
        this.d = d;
    }
}
